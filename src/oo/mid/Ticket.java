package oo.mid;

public class Ticket {
	int id;
	String name;
	float price;
	//setter設定屬性值  getter取得屬性值
		public void setId(int id){
			this.id = id;
		}
		public int getId(){
			return id;
		}
		public void setName(String name){
			this.name = name;
		}
		public String getName(){
			return name;
		}
		public void setPrice(float price){
			this.price = price;
		}
		public Float getPrice(){
			return price;
		}
	}


